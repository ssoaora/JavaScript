const device = 'Macbook';

switch (device) {
  case 'iPhone':
    console.log('You are using iPhone.');
    break;

  case 'iPad':
    console.log('You are using iPad.');
    break;

  case 'iMac':
    console.log('You are using iMac.');
    break;

  default:
    console.log('You are using an unknown device.');
    break;
}
